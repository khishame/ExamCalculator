package mark;


public abstract class Mark {
    private int value;

    public Mark(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
    
    public abstract double contribution();
    
}
